	LD	A,(MAJOR_VER)
	CP	2
	JR	NC,REN_DOS2
	CALL	SETFCB
	LD	A,010H		;ディレクトリも対象にする(LSX-Dodgers)
	LD	(FCB1+13),A	;属性
	LD	C,017H		;(BDOS)ファイル名の変更
CDEL1:
	LD	DE,FCB1
REN2:
	CALL	SYSTEM
	ADD	A,0FFH
	RET

REN_DOS2:			;DOS2のリネーム(ディレクトリ対応)
	CALL	SPCUT
	LD	L,E
	LD	H,D
REN_D2_1:
	LD	A,(HL)
	CP	020H+1
	JR	C,REN_D2_2
	INC	HL
	JR	REN_D2_1
REN_D2_2:
	LD	(HL),0
	INC	HL
	LD	C,04EH		;(BDOS)ファイルあるいはサブディレクトリ名の変更(_RENAME)
