	CP	021H
	JR	NC,CPATH0

	LD	HL,N_PATH
	LD	DE,V_PATH
	LD	B,PATHX
	LD	C,06BH		;_GENV 環境変数の獲得
	CALL	SYSTEM
	LD	HL,V_PATH
