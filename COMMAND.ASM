	CALL	DEFCB
	JP	NZ,C_BAT
	LD	DE,DATA_COM
	CALL	SETDTA

	LD	HL,(SYSTEM+1)
	LD	DE,DATA_COM
	SBC	HL,DE

	CALL	S27DTA
	DEC	A
	SCF
	RET	NZ
	LD	A,H
	OR	L
	SCF
	RET	Z
	LD	(COMSIZ),HL

	LD	DE,DATA_COM
	CALL	SETDTA
	LD	DE,0
COMPAT	EQU	$-2
	CALL	SETFCB

	LD	HL,N_APPEND
	LD	DE,PATH_CCP
	LD	B,PATHX
	LD	C,06BH		;_GENV 環境変数の獲得
	CALL	SYSTEM
	LD	A,(PATH_CCP)
	OR	A
	JR	NZ,COM_ENV1
	LD	C,019H		;(BDOS)カレントドライブ番号の獲得(_CURDRV)
	CALL	SYSTEM
	ADD	A,'A'
	LD	(PATH_CCP),A
	LD	A,':'
	LD	(PATH_CCP+1),A
	LD	A,05CH		;\
	LD	(PATH_CCP+2),A
	LD	B,A		;ドライブ
	LD	DE,PATH_CCP+2
	LD	C,059H		;(BDOS)GETCD カレントディレクトリの獲得
	CALL	SYSTEM
COM_ENV1:
	LD	DE,DTA_CCP
	LD	HL,DOS2NAME
	CALL	FCB2FNAME
	LD	DE,PATH_CCP
	LD	HL,DOS2NAME+2
COM_ENV2:
	LD	A,(DE)
	OR	A
	JR	Z,COM_ENV3
	INC	DE
	JR	COM_ENV2
COM_ENV3:
	DEC	DE
	LD	A,(DE)
	INC	DE
	CP	05CH
	JR	Z,COM_ENV4
	LD	A,05CH
	LD	(DE),A
	INC	DE
COM_ENV4:
	LD	A,(HL)
	INC	HL
	LD	(DE),A
	INC	DE
	OR	A
	JR	NZ,COM_ENV4

	LD	HL,N_PROGRAM
	LD	DE,PATH_CCP
	LD	C,06CH		;_SENV 環境変数の設定
	CALL	SYSTEM

	LD	HL,N_PARAMETERS
	LD	DE,DTA1+1
	LD	C,06CH		;_SENV 環境変数の設定
	CALL	SYSTEM

	LD	HL,N_APPEND
	LD	DE,V_APPEND
	LD	C,06CH		;_SENV 環境変数の設定
	CALL	SYSTEM

	LD	C,60H		;(BDOS)子プロセスの起動
	CALL	SYSTEM
	OR	A
	JR	NZ,COMP1
	LD	A,B
	LD	(PROCESS),A
COMP1:
	XOR	A
	LD	H,A
	LD	L,A	;HL=0x0000
	LD	(TRCFLG),A
	DI
	LD	SP,(SYSTEM+1)
	PUSH	HL
	LD	HL,0B0EDH	;LDIR
	LD	(000FEH),HL
	LD	HL,DATA_COM
	LD	DE,00100H
	LD	BC,0
COMSIZ	EQU	$-2
	EI
	JP	000FEH

C_BAT:
	LD	HL,DTA_CCP+9
	LD	(HL),'B'
	INC	HL
	LD	(HL),'A'
	INC	HL
	LD	(HL),'T'

	CALL	DEFCB
	RET	NZ

	LD	HL,DTA_CCP
	LD	A,(HL)
	OR	A
	JR	NZ,CBAT_DS
	PUSH	HL
	LD	C,019H		;(BDOS)カレントドライブ番号の獲得(_CURDRV)
	CALL	SYSTEM
	POP	HL
	INC	A
	LD	(HL),A
CBAT_DS:
	LD	DE,(FCB_BAT)
	LD	BC,37
	LDIR
C_BAT1:
	CALL	SETDTA100
	CALL	FGETC_BAT
	LD	HL,DTA1+1
	JR	NZ,END_BATCH
	CP	021H		;スペースや改行など制御文字を飛ばす
	JR	C,C_BAT1
	LD	(HL),' '
	INC	HL
C_BAT2:
	LD	(HL),A
	INC	HL
	LD	A,L
	INC	A		;L==0FFH
	JR	Z,RUN_BATCH
	CALL	FGETC_BAT
	JR	NZ,RUN_BATCH
	CP	020H
	JR	NC,C_BAT2
