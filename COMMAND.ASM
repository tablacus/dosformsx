	LD	A,L
	SUB	DTA1-1
	LD	(DTA1),A
	CP	4
	JR	C,END_BATCH
	LD	(HL),0
	LD	A,(INTFLG)
	CP	3
	JP	NZ,COMMAND2
END_BATCH:		;バッチファイルを閉じる
	LD	HL,(FCB_BAT)
	LD	(HL),0
	LD	SP,(SYSTEM+1)
	JP	WSTART

FGETC_BAT:
	LD	DE,(FCB_BAT)
FGETC:				;1文字ずつ読み込む
	PUSH	HL		;Z:成功
	LD	HL,1
	CALL	S27
	POP	HL
	LD	A,(DATA)
	RET

C_REM:
	XOR	A
	RET

C_DEL:
	CALL	SETFCB
	LD	C,8		;(BDOS)エコーなしコンソール入力
	CALL	SYSTEM
	LD	B,0
	LD	A,(MAJOR_VER)
	CP	2
	JR	C,CDEL_DOS1

	LD	HL,FCB1
	CALL	OPEN_D2
	SCF
DEL1:
	RET	NZ
	LD	A,(SEARCH_IX+14);	(FIB)属性
	BIT	4,A
	JR	NZ,DEL2

	LD	DE,SEARCH_IX
	LD	C,04DH		;(BDOS)ファイル・サブディレクトリの削除(_DELETE)
	CALL	SYSTEM0
	SCF
	RET	NZ
DEL2:
	CALL	OPEN2_D2
	JR	DEL1

CDEL_DOS1:
	LD	C,013H
	JR	CDEL1		;(BDOS)ファイルの削除

