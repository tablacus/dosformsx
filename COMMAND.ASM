	LD	C,027H		;(BDOS)ランダム・ブロック・リード
	CALL	SYSTEM
	OR	A
	RET

CEXE4:
	LD	HL,FDRV
	LD	DE,DTA_CCP
	LD	BC,1+8
	LDIR
	LD	HL,DTA_CCP+9
	LD	(HL),'C'
	INC	HL
	LD	(HL),'O'
	INC	HL
	LD	(HL),'M'
