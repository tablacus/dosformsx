	LD	IX,SEARCH_IX
	LD	BC,01040H	;(BDOS)最初のエントリ検索
	CALL	SYSTEM
	PUSH	AF
	LD	DE,PATH_PAR
	LD	C,05EH		;(BDOS)完全なパス文字列の獲得(_WPATH)
	CALL	SYSTEM
	XOR	A
	LD	(HL),A
	LD	DE,PATH_PAR
	SBC	HL,DE
	LD	(PATH_POS),HL
	POP	AF
FIB2DIRENT:
	OR	A
	RET	NZ

	LD	DE,SEARCH_IX+1	;(FIB)ファイル名
	LD	HL,DTA_CCP+1
	LD	C,05CH		;(BDOS)ファイル名の解析
	CALL	SYSTEM

	LD	HL,SEARCH_IX+14	;(FIB)属性
	LD	DE,DTA_CCP+1+8+3
	LDI
	LD	DE,DTA_CCP+1+8+3+1+10
	LD	BC,2+2+2+4	;最終変更時刻+最終変更日付+開始クラスタ+ファイルサイズ
	LDIR
	XOR	A
	RET

OPEN1:
	LD	HL,FDRV
OPEN:
	LD	C,011H		;(BDOS)最初のファイルの検索
OPEN3:
	PUSH	DE
	LD	DE,DTA_CCP
	CALL	SETDTA
	EX	DE,HL
	CALL	SYSTEM
	POP	DE
	OR	A
	RET

OPEN2_D2:			;DOS2対応
	LD	A,(MAJOR_VER)
	CP	2
	JR	C,OPEN2
	LD	IX,SEARCH_IX
	LD	C,41H		;(BDOS)次のエントリ検索
	CALL	SYSTEM
	JR	FIB2DIRENT
OPEN2:
	LD	C,012H
	JR	OPEN3

DEFCB_D2:			;DOS2対応
	LD	A,(MAJOR_VER)
	CP	2
	JR	C,DEFCB
	LD	HL,PATH_PAR
	LD	DE,PATH_CCP
	LD	BC,64
	LDIR
	LD	HL,(PATH_POS)
	LD	DE,PATH_CCP
	ADD	HL,DE
	LD	B,13
	LD	DE,SEARCH_IX+1	;(FIB)ファイル名
DCB4:
	LD	A,(DE)
	OR	A
	JR	Z,DCB5
	INC	DE
	LD	(HL),A
	INC	HL
	JR	DCB4
DCB5:
	LD	(HL),0
	LD	A,(SEARCH_IX+25)	;(FIB)ドライブ番号(1-8)
	ADD	A,040H
	LD	DE,PATH_DRV
	LD	(DE),A
	LD	C,043H		;_OPEN ファイルハンドルのオープン
	LD	A,1		;書き込み禁止
	CALL	SYSTEM
	OR	A
	RET	NZ
	LD	(HANDLE1),BC
	JR	SETDTA100
DEFCB:				;Z=Ok NZ=Error
	LD	DE,DTA_CCP
	CALL	SYSC0F
	OR	A
	RET	NZ

	LD	HL,1
	LD	(DTA_CCP+14),HL
	DEC	L
	LD	(DTA_CCP+33),HL
	LD	(DTA_CCP+35),HL
SETDTA100:
	LD	DE,DATA
