	LD	A,(MAJOR_VER)
	CP	2
	JR	C,S27DTA
	LD	BC,(HANDLE1)
	LD	DE,DATA
	LD	C,048H		;(BDOS)ファイルハンドルからの読み出し(_READ)
	CALL	SYSTEM0
	RET	Z
	CP	0C7H		;.EOF
	JR	Z,S27EOF
	SCF
	SBC	A,A
	RET
