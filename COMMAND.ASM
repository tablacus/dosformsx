	CALL	FILEC_D2
	LD	DE,PATH_CCP
	LD	A,(DE)
	CP	020H
	JR	C,PWD
	LD	C,5AH		;(BDOS)_CHDIR カレントディレクトリの変更
	JP	SYSTEM
