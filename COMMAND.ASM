C_DIR:
	CALL	FILEC_D2
	LD	HL,FDRV+1
	CALL	CWILD1
	LD	A,0F1H
	LD	(FDRV+13),A
	CALL	OPEN1_D2
CDIR1:
	OR	A
	JR	NZ,PDSKF
	CALL	P_NAME
	CALL	OPEN2_D2
	JR	CDIR1

PDSKF:
	LD	A,(FDRV)
	LD	E,A
	LD	C,01BH		;(BDOS)ディスク情報の獲得
	CALL	SYSTEM
	LD	C,A		;C←1クラスタ辺りの論理セクタ数
	ADD	A,001H
	RET	C		;Aが0FFHだった場合
	LD	A,8-2
PDS1:				;HL=未使用クラスタの総数
	INC	A
	RR	C
	JR	NC,PDS1
PDS2:				;B←論理セクタのサイズの上位8ビット
	INC	A
	RR	B
	JR	NC,PDS2
	LD	B,A

	LD	DE,0
PDS3:
	ADD	HL,HL
	EX	DE,HL
	ADC	HL,HL
	EX	DE,HL
	DJNZ	PDS3
PDSKF1:
	CALL	PRDEC_DEHL
	LD	DE,FREE
	LD	C,9		;(BDOS)文字列出力
	CALL	SYSTEM
	CALL	PUTDRV
	LD	A,05CH		;\
	CALL	MSG_A
	LD	A,(MAJOR_VER)
	CP	1
	JR	C,LTNL
	JR	NZ,PDSKF2
	LD	HL,(FDRV+14)
	XOR	A
	LD	DE,0-2
	ADD	HL,DE
	INC	HL
	CALL	C,PRDEC_HL
	JR	LTNL
PDSKF2:				;MSX-DOS2
	LD	A,(FDRV)
	LD	B,A		;ドライブ
	LD	DE,DATA
	LD	C,059H		;(BDOS)GETCD カレントディレクトリの獲得
	CALL	SYSTEM
	LD	HL,DATA

	CALL	MSX
	JR	LTNL

MSX1:
	CALL	MSG_A
MSX:
	LD	A,(HL)
	INC	HL
	OR	A
	JR	NZ,MSX1
	RET

MSXSLT1:
	CALL	MSG_A
