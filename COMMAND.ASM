	LD	A,(FDRV)
	OR	A
	JR	NZ,PUTDRV1
	PUSH	BC
	PUSH	DE
	PUSH	HL
	LD	C,019H
	CALL	SYSTEM
	POP	HL
	POP	DE
	POP	BC
	INC	A
