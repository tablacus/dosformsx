CSET1:
	PUSH	DE
	LD	BC,0FF6DH	;_FENV 環境変数の検索
	LD	HL,DATA_COM
	LD	(HL),0
	CALL	SYSTEM
	OR	A
	JR	NZ,CSET_ERROR
	LD	HL,DATA_COM
	LD	A,(HL)
	OR	A
	JR	Z,CSET_ERROR
	CALL	MSX
	LD	E,'='
	CALL	_PRINT
	LD	HL,DATA_COM
	LD	DE,DATA_COM+256
	LD	BC,0FF6BH	;_GENV 環境変数の獲得
	CALL	SYSTEM
	LD	HL,DATA_COM+256
	CALL	MSX
	CALL	LTNL
	LD	A,10
	LD	(CSET_DATA),A
