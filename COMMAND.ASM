DATA	EQU	08000H

	ORG	0100H
	JP	START
VER:
	DB	00DH,00AH,"COMMAND v0.17",00DH,00AH,'$'
START:
	LD	SP,(SYSTEM+1)
	LD	C,06FH		;(BDOS)MSX-DOS のバージョン番号の獲得
	CALL	SYSTEM
	RET	C
	LD	HL,0011DH
	SBC	HL,BC
	LD	A,B
	LD	(MAJOR_VER),A
	OR	L
	LD	(IS_MSX),A
	OR	A
	JR	Z,WSTART

	LD	A,(PROCESS)
	LD	C,61H		;(BDOS)親プロセスに戻る
	LD	B,A
	OR	A
	CALL	NZ,SYSTEM
	XOR	A
	LD	(PROCESS),A

	LD	HL,002BH	;キャラクターセット・日時フォーマット・ビデオ信号の種別
	CALL	RDSLT
	RRCA
	RRCA
	RRCA
	RRCA
	AND	3
	JR	Z,DATEOK
	DEC	A
	LD	HL,V_DATE1
	JR	Z,DATECHG
	DEC	A
	LD	HL,V_DATE2
	JR	NZ,DATEOK
DATECHG:
	LD	DE,V_DATE
	LD	BC,8
	LDIR
DATEOK:
	CALL	INIT_VAR
	LD	A,(TRCFLG)
	OR	A
	JR	Z,WSTART0
	LD	DE,COMERM
	LD	C,9		;(BDOS)文字列出力
	CALL	SYSTEM
	LD	HL,(TRCFLG)
	LD	H,0
	CALL	PRDEC_HL
	XOR	A
	LD	(TRCFLG),A
WSTART0:
	LD	HL,(SYSTEM+1)
	LD	L,256-37
	INC	H
	LD	(FCB_BAT),HL
	LD	A,(DTA1)
	OR	A
	JR	Z,WSTART
	LD	DE,VER
	LD	C,9
	CALL	SYSTEM
	LD	DE,DTA1+1
	CALL	COMANL
WSTART:
	LD	HL,N_APPEND
	LD	DE,V_APPEND
	LD	C,06CH		;_SENV 環境変数の設定
	CALL	SYSTEM

	LD	HL,N_PROGRAM
	LD	DE,ZERO
	LD	C,06CH		;_SENV 環境変数の設定
	CALL	SYSTEM

	LD	HL,N_PARAMETERS
	LD	DE,ZERO
	LD	C,06CH		;_SENV 環境変数の設定
	CALL	SYSTEM

	CALL	LTNL
COMMAND:
	PUSH	HL
	LD	HL,(FCB_BAT)
	LD	A,(HL)
	POP	HL
	OR	A
	JP	NZ,C_BAT1
	CALL	SETDTA1
	PUSH	DE
	PUSH	BC
	PUSH	HL
	LD	C,019H		;(BDOS)_CURDRV デフォルトドライブの獲得
	CALL	SYSTEM
	POP	HL
	POP	BC
	ADD	A,'A'
	CALL	MSG_A
	LD	E,'>'
	CALL	_PRINT
	POP	DE		;DE=DTA1
	LD	A,80
	LD	(DE),A
	LD	C,0AH		;(BDOS)文字列入力
	CALL	SYSTEM
	CALL	LTNL
COMMAND2:
	LD	DE,DTA1+2
	CALL	COMANL
	JR	NC,COMMAND
	LD	DE,COMERM
	LD	C,9		;(BDOS)文字列出力
	CALL	SYSTEM
	JR	WSTART

COMANL:
	CALL	TOZERO
	LD	(COMST),DE
	CALL	FILE
	PUSH	DE
	LD	DE,FNAME
	LD	A,(DE)
	CP	020H
	JP	Z,SDVSW
	DEC	DE
	LD	A,(DE)
	ADD	A,0FFH
	JR	C,COMB
	INC	DE

	LD	HL,COMTB
	LD	A,COMS
	LD	B,A
	CALL	CPNAME
COMB:
	POP	DE
	JP	NC,JP_HL
COMB2:
	EX	DE,HL
	LD	(COMPAT),HL

	LD	DE,0
COMST	EQU	$-2
	LD	HL,DATA_COM
	LD	(HL),0
COMPR1:
	LD	A,(DE)
	INC	DE
	INC	HL
	LD	(HL),A
	CP	020H
	JR	NC,COMPR1
COMPR2:
	DEC	HL
	LD	A,(HL)
	CP	05CH
	JR	NZ,COMPR3
	LD	(HL),0
	INC	HL
	EX	DE,HL
	CALL	FILE
	LD	HL,N_APPEND
	LD	DE,DATA_COM+1
	LD	C,06CH		;_SENV 環境変数の設定
	CALL	SYSTEM
	JR	COMPR4
COMPR3:
	CP	020H
	JR	NC,COMPR2
COMPR4:
	CALL	CEXE4

	LD	HL,N_PATH
	LD	DE,V_PATH
	LD	B,PATHX
	LD	C,06BH		;_GENV 環境変数の獲得
	CALL	SYSTEM

	LD	HL,V_PATH
	LD	(X_PATH),HL
CEX1:
	LD	HL,(X_PATH)
	LD	A,(HL)
	CP	020H
	RET	C
CEX2:
	LD	A,(HL)
	CP	020H
	JR	C,CEX3
	CP	';'
	JR	Z,CEX3
	INC	HL
	JR	CEX2
CEX3:
	LD	(HL),0
	CP	020H
	JR	C,CEX4
	INC	HL
CEX4:
	LD	DE,(X_PATH)
	LD	(X_PATH),HL
	LD	HL,N_APPEND
	LD	C,06CH		;_SENV 環境変数の設定
	CALL	SYSTEM

	CALL	CEXE4
	JR	CEX1

SDVSW:
	POP	AF
	LD	A,(FDRV)
	DEC	A
	LD	E,A
	LD	C,00EH		;(BDOS)カレントドライブの設定
	JP	SYSTEM0

OPEN_D2:			;DOS2対応
	LD	A,(MAJOR_VER)
	CP	2
	JR	C,OPEN
	LD	DE,PATH_FN1
	JR	OPEN_D2X
OPEN1_D2:			;DOS2対応
	LD	A,(MAJOR_VER)
	CP	2
	JR	C,OPEN1

	LD	DE,PATH_CCP
