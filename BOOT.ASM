;	似非DOS BOOT

	BDOS	EQU	0F37DH

	ORG	0C000H
;
;	BPB
;
	DB	0EBH,0FEH,90H,"DOS  "
	DB	"   ",00H,02H,02H,01H,00H
	DB	02H,70H,00H,0A0H,05H,0F9H,03H,00H
	DB	09H,00H,02H,00H,00H,00H

	RET	NC

	LD	DE,FCB
	LD	C,00FH
	CALL	BDOS

	LD	DE,00100H
	LD	C,01AH		;(BDOS)DTAの設定
	CALL	BDOS

	LD	HL,0
	LD	(FCB+33),HL
	LD	(FCB+35),HL
	INC	L
	LD	(FCB+14),HL

	LD	DE,FCB
	LD	HL,0BF00H
	LD	C,027H		;(BDOS)ランダム・ブロック・リード
	CALL	BDOS

	JP	0100H

FCB:	DB	0, "DOS     SYS",0,0,0,0
